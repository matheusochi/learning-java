package streams;

public class Car {
    final String brand;
    final String model;
    final String color;
    final Integer year;
    final Double kilometersDriven;

    public Car(String brand, String model, String color, Integer year, Double kilometersDriven) {
        this.brand = brand;
        this.model = model;
        this.color = color;
        this.year = year;
        this.kilometersDriven = kilometersDriven;
    }
}
